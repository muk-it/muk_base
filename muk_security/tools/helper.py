class NoSecurityUid(int):

    def __int__(self):
        return self

    def __eq__(self, other):
        if isinstance(other, int):
            return False
        return super(NoSecurityUid, self).__int__() == other

    def __hash__(self):
        return super(NoSecurityUid, self).__hash__()
